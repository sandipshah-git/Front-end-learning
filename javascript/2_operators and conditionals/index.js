//Pre increment/decrement
let x = 10;
let y = 4;

console.log(++x);    // 11    (it's work like, print first increament value and then print real value )
console.log(--y);    // 3    (print first descrement value and then print real one)
console.log(x);      // 11
console.log(y);      // 3

//Post increment/decrement
let a = 10;
let b = 4;
console.log(a++);      // 10    (printing first real value )
console.log(b--);      // 4

console.log(a);        // 11   (and then print increment value)
console.log(b);        // 3